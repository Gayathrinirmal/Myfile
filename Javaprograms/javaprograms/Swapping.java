package javaprograms;

public class Swapping {

	public static void main(String[] args) {
		int a=40,b=50,c=0;
		System.out.println("Before swapping values are a : " + a +", b : "+ b);	
        c=a;
        a=b;
        b=c;
    	System.out.println("After swapping values are a : " + a +", b : "+ b);
	}

}
